import { Box, Container } from '@chakra-ui/react'
import Head from 'next/head'
import { useRouter } from 'next/router'
import { useEffect } from 'react'
import { useDispatch, useSelector } from 'react-redux'
import { collection, getDocs, query, where } from 'firebase/firestore'
import { db } from '../../firebase.config'
import { addProfile, resetLoading } from '../../redux/slice/profileSlice'
import ProfileBreadCrumb from '../../components/pages/profile/ProfileBreadCrumb'
import ProfileContainer from '../../components/pages/profile/ProfileContainer'

export default function Profile() {
    const { profile, trigger } = useSelector((sta) => sta.profile)
    const dispatch = useDispatch()
    const router = useRouter()

    // fetch course detail
    useEffect(() => {
        async function getProfile() {
            try {
                // query uid in users collection
                const slugUid = router.query.param[0]
                const q = query(
                    collection(db, 'users'),
                    where('uid', '==', slugUid)
                )
                const result = await getDocs(q)
                if (result.docs.length > 0) {
                    const doc = result.docs[0]
                    const userDoc = { _id: doc.id, ...doc.data() }
                    // console.log(userDoc)
                    // Redirect in to overview
                    router.push(`/profile/${slugUid}/overview`)
                    dispatch(addProfile(userDoc))
                } else {
                    console.log('user not found')
                    router.push('/404')
                }
            } catch (error) {
                console.log(error)
            }
        }

        getProfile()

        return () => {
            dispatch(resetLoading())
        }
    }, [trigger, dispatch])

    return (
        <>
            <Head>
                <title>
                    {`Hồ sơ ${profile?.displayName}` || 'Đang tải'} | Tridev.io
                    - Học lập trình miễn phí | Học lập trình cho người mới
                </title>
                <meta
                    name='description'
                    content='Generated by create next app'
                />
                <link rel='icon' type='image/png' href='/code.png' />
            </Head>
            <Box pb='4' pt='8'>
                <Container maxW={[1024]}>
                    {/* BreadCrumb  */}
                    <ProfileBreadCrumb />
                    {/* Container  */}
                    <ProfileContainer />
                </Container>
            </Box>
        </>
    )
}
